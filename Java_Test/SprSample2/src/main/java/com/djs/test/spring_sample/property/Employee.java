
package com.djs.test.spring_sample.property;

public class Employee
{
	private String name;
	private float salary;

	public String getName()
	{
		return this.name;
	}

	public void setName( String name )
	{
		this.name = name;
	}

	public float getSalary()
	{
		return salary;
	}

	public void setSalary( float salary )
	{
		this.salary = salary;
	}
}
